        <p>Salah satu komitmen kami adalah setiap hasil jahitan Rumah Jahit MDS memiliki jahitan berkualitas
            butik. Berikut ini adalah beberapa ciri-ciri jahitan berkualitas butik di MDS :
        </p>
        <ol class="list-decimal pl-4 mt-5">
            <li>Menggunakan stik kecil/halus. Semakin kecil stik yang digunakan maka jahitan semakin rapat -> makin
                kuat.
                1 cm = 4 s/d 6 stik</li>
            <li>Obrasan rapi, tidak mengkerut (untuk obras kami menggunakan benang katun, bukan benang obras)</li>
            <li>Jahitan tidak mengambang/loncat</li>
            <li>Menggunakan material yang berkualitas bagus, misalnya benang dan resleting (kami tidak menggunakan
                resleting Taiwan)</li>
            <li>
                <span>Mengikuti standar pola Internasional , untuk wanita :</span>
                <ul class="list-disc pl-4">
                    <li>lebar badan muka ditambah 1 cm dari lebar belakang</li>
                    <li>kerung lengan depan lebih masuk 1 cm dari belakang (jahitan lengan maju 1 cm dari jahitan
                        samping badan)</li>
                    <li>kampuh (sisa jahitan dalam minimal 2 cm)</li>
                </ul>
            </li>
            <li>Presisi : ketepatan dalam pengerjaan terutama pada bagian yang tampak dari luar seperti : resleting,
                krag
                dan lubang kancing</li>
            <li>Untuk kain bermotif, potongan motif harus pas/ketemu</li>
            <li>Untuk pakaian wanita menggunakan model ber-kupnat* (* tidak semua – tergantung model)</li>
            <li>Krag menggunakan dua lapisan yaitu visiline dan staplex kecuali kerah rebah dan frill</li>
            <li>Bagian keliman baju atau lengan di “sum”/ tidak dijahit, kecuali untuk bahan tertentu sepeti denim,
                sifone dan sejenisnya (dijahit lurus stick kecil) atau pada bagian bawah gamis</li>
        </ol>
