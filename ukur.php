        Cara Ukur Badan
    </h2>
    <br>
    <div class="text-justify text-black/80">

        <p>
            Mengukur badan merupakan salah satu langkah paling penting dalam proses menjahit pakaian “custom-made” atau
            “made-to-order” di Rumah Jahit MDS.

            Perbedaan beberapa centimeter saja pada tubuh anda bisa membuat perbedaan besar pada pakaian yang anda
            kenakan. Misalnya, pakaian yang terlalu besar membuat seorang wanita dewasa terlihat seperti anak-anak yang
            sedang mengenakan pakaian dewasa, atau terlihat seperti pakaian pinjaman.

            Selain masalah penampilan, ukuran badan juga bisa mempengaruhi keawetan kain. Misal, untuk yang berbadan
            besar, jika dibeberapa bagian ukuran pakaiannya terlalu kecil, akan membuat gerak tubuh terbatas dan jika
            dipaksakan akan membuat kain dan jahitan robek. Atau jika pakaiannya terlalu panjang, akan mudah terinjak
            dan menjadi cepat rusak.

            Meskipun pakaian yang akan dijahitkan di MDS adalah pakaian muslimah yang tidak ketat dan menonjolkan lekuk
            tubuh, bukan berarti tidak memerlukan ukuran badan yang akurat. Ukuran badan ini dibutuhkan untuk membuat
            pakaian tersebut nyaman digunakan, terutama pada bagian-bagian berikut : bahu, kerung lengan, lingkar badan,
            lingkar pinggang dan pinggul.
        </p>
