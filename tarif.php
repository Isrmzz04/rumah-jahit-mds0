Biaya jahit di Rumah Jahit MDS itu sangat tergantung dengan  model dan bahannya, model sebuah busana itu bisa bermacam-macam, bisa model yang simple bisa juga banyak pecah pola, dari cutting simple , cutting agak ribet atau cutting sangat ribet ^_^, aksennya seperti apa, jahit opnaisel, frill ?, bukaan menyusui, payet ? kalau di analogikan, biaya jahit itu seperti anda membeli sebuah mobil, Avanza misalnya, yang model paling standar, tanpa power steering, tanpa power window, ac single, audio standar, tentu berbeda harganya jika anda membeli dengan kelengkapan lebih, tambah electric mirror, jok kulit, doubel din, double blower, velg racing, dll, dll. 
<br>
Itu belum soal bahannya, bahan katun biasa tentu berbeda ongkos jahitnya dengan bahan sutra misalnya, pakai vuring atau tidak ? pakai tricod atau tidak ? dan banyak lagi komponen-komponen yang mempengaruhi biaya jahit di RJH. 
<br>
Bahan batik berbeda ongkos jahitnya dengan bahan polos, tingkat kesulitan membuat pola dan memotong bahan (supaya motif batiknya ketemu) membuatnya sedikit lebih tinggi daripada bahan polos biasa, batik tulis juga berbeda dengan batik cap atau printing, semakin mahal harga bahan, tentu saja ongkos jahitnya semakin mahal pula, karena resiko yang kami tanggung juga semakin tinggi ^_^.
<br>
Mudah-mudahan anda jadi paham kan kalau jawaban untuk pertanyaan berapa ongkos jahit di RJH itu tergantung model dan bahannya, dan kami biasanya meminta anda untuk mengirimkan contoh model atau detail order jahitan anda (biasanya melalui email) supaya kami bisa lebih jelas soal model busana yang ingin anda jahitkan di RJH.
<br>
Dan harap anda ketahui juga, bahwa di RJH, kami memiliki standar kualitas tersendiri untuk jahitan dan material yang digunakan. Untuk kualitas jahitan yang kami berikan adalah kualitas jahitan butik atau jahitan halus, ciri-cirinya bisa anda baca di artikel kami yang mengulas soal standar kualitas jahitan di RJH.
<br>
Material yang kami gunakan juga memiliki standar tersendiri, antara lain : kami tidak menggunakan resleting taiwan melainkan resleting jepang (YKK dan KCC – bukan rislit abal-abal), furing yang kami gunakan adalah hero/arrow bukan SPTI atau Asahi, payet yang kami gunakan adalah payet jepang, staplek, visilin, benang jahit ?? yang terbaik yang kami gunakan ^_^.
<br>
Dan hal penting lainnya yang kami harap anda ketahui adalah, bahwa setiap customer kami buatkan polanya masing-masing sesuai dengan ukuran tubuhnya*, jadi tidak menggunakan pola standar SML alias pola pukul rata (pola hajar abis – red) untuk semua.
<br>
