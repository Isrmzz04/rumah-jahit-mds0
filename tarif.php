<br>
– Ongkos jahit gamis mulai dari Rp 300.000*.
<br>
– Ongkos jahit blouse/atasan/kemeja mulai dari Rp 200.000*.
<br>
– Ongkos jahit rok mulai dari Rp 175.000*.
<br>
– Ongkos jahit kebaya mulai dari Rp 400.000*.
<br>
– Ongkos jahit baju anak mulai dari Rp 175.000*.
<br>
